#include "libft.h"

/*
** Returns the length of string `s` (number of chars before NUL).
*/

size_t	ft_strlen(const char *s)
{
	size_t	len;

	if (!s)
		return (0);
	len = 0;
	while (s[len])
		len++;
	return (len);
}
