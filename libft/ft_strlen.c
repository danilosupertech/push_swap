#include "libft.h"

/*
** Retorna o tamanho da string `s` (n√∫mero de chars antes do NUL).
*/
size_t	ft_strlen(const char *s)
{
	size_t	len;

	if (!s)
		return (0);
	len = 0;
	while (s[len])
		len++;
	return (len);
}
